#  Collections are also called Data Structure
# 1. Sequences - string, list, tuple
# 2. Mappings - dictionary
# 3. Sets - set, Frozenset

# String - Ordered, immutable sequences of characters
#     - "Hello World"

# List - Ordered, mutable collections that can contain mixed data types
#       -  [1, 'hello', 3.14, True]

# Tuple  - Ordered immutable collections similar to lists
#         - (1, 'hello', 3.14, True)

# Dictionary  - Unordered (pre-3.7) / insertion-ordered 3.7+) key-value pairs
#             -  {'name': 'Alice', 'age':25, 'city': 'New York'}

# Set - Unordered mutable collections of unique elements(Repetition not allowed)
#    -  {1,2,3,4}
print({12,4,5,5,6,8})

